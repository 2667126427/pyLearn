name = input("input the file name:")
words = []
with open(name, 'r') as f:
    lines = f.readlines()
    for line in lines:
        for item in line.strip():
            words.append(m[item] + ' ')
        words.append('\n')

with open(name + '.out.txt', 'w') as f:
    for i in words:
        f.write(i)
